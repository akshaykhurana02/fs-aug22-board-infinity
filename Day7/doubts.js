// Left Shift <<

// Shifts bits to the left and attach 0 instead from the right)

console.log("5 << 1 -> ", 5 << 1); // 10
/*

00000000000000000000000000000101 -> 5
00000000000000000000000000001010 -> 10
*/

console.log("5 << 2 -> ", 5 << 2); // 20
/*

00000000000000000000000000000101 -> 5
00000000000000000000000000010100 -> 20

*/

console.log("5 << 3 -> ", 5 << 3); // 40
/*

00000000000000000000000000000101 -> 5
00000000000000000000000000101000 -> 40

*/

// Right Shift >>

// >> Sign Preserving Shift

console.log("5 >> 1 -> ", 5 >> 1);

/*

00000000000000000000000000000101 -> 5
00000000000000000000000000000010 -> 2

*/

console.log("-5 >> 1 -> ", -5 >> 1);

/*

11111111111111111111111111111011 -> -5
11111111111111111111111111111101 -> -3

00000000000000000000000000000010 (2's Complement -> Reversing all the bits)
00000000000000000000000000000001 (Adding 1)
00000000000000000000000000000011 -> -3

*/

// >>> Zero Filling Shift
// This will always generate a positive number

console.log("5 >>> 1 -> ", 5 >>> 1);

/*

00000000000000000000000000000101 -> 5
00000000000000000000000000000010 -> 2

*/

console.log("-5 >>> 1 -> ", -5 >>> 1);

/*

11111111111111111111111111111011 -> -5
01111111111111111111111111111101 -> 2147483645

*/
